--
-- 功能：按 phones.id 删除多余号码（去重时常用）
-- 用法：① 将 ${ids} 改为具体 id，或配置 DBeaver 绑定变量
--       ② 执行「预览」段  ③ 确认后单独选中「写入」段执行  ④ Ctrl+S 提交
-- =============================================================================

-- ── 预览（只读，可先执行）────────────────────────────────────────────────────

SELECT p.id AS phone_id, c.fn, p.number, p.types
FROM phones p
JOIN contacts c ON p.contact_id = c.id
WHERE p.id IN (${ids})
ORDER BY p.id;

-- 不用变量时，直接写 id 列表，例如：
-- WHERE p.id IN (496, 501, 507);


-- =============================================================================
-- ⚠️⚠️⚠️  以下会写入数据库 — 确认预览结果后再执行此段  ⚠️⚠️⚠️
-- =============================================================================

DELETE FROM phones
WHERE id IN (${ids});

