-- =============================================================================
-- ⚠️  写入操作 — 会修改数据库
--
-- 功能：将 596 开头的号码补 0 前缀（596… → 0596…）
-- 用法：① 执行「预览」段  ② 确认后单独选中「写入」段执行  ③ Ctrl+S 提交
-- =============================================================================

-- ── 预览（只读，可先执行）────────────────────────────────────────────────────

SELECT
    p.id AS phone_id,
    c.fn,
    p.number AS 改前,
    '0' || p.number AS 改后,
    p.types
FROM phones p
JOIN contacts c ON p.contact_id = c.id
WHERE p.number LIKE '596%'
ORDER BY p.number;


-- =============================================================================
-- ⚠️⚠️⚠️  以下会写入数据库 — 确认预览结果后再执行此段  ⚠️⚠️⚠️
-- =============================================================================

UPDATE phones
SET number = '0' || number
WHERE number LIKE '596%';
